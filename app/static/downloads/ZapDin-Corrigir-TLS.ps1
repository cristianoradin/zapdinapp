function Add-Root($pem, $label) {
    $tmp = Join-Path $env:TEMP ("isrg-" + $label + ".crt")
    Set-Content -Path $tmp -Value $pem -Encoding Ascii
    try {
        Import-Certificate -FilePath $tmp -CertStoreLocation Cert:\LocalMachine\Root -ErrorAction Stop | Out-Null
        Write-Host ("  [OK] " + $label + " importado (Import-Certificate)") -ForegroundColor Green
    } catch {
        & certutil -addstore -f Root $tmp | Out-Null
        if ($LASTEXITCODE -eq 0) {
            Write-Host ("  [OK] " + $label + " importado (certutil)") -ForegroundColor Green
        } else {
            Write-Host ("  [FALHA] " + $label + " nao importado") -ForegroundColor Red
        }
    }
    Remove-Item $tmp -ErrorAction SilentlyContinue
}

